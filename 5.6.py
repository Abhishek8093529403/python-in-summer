#!/usr/bin/python
'''stages of life (if-elif-else chain)'''
age = 19
print("my age is 19.")
if age < 2:
    print("You're a baby!")
elif age < 4:
    print("You're a toddler!")
elif age < 13:
    print("You're a kid!")
elif age < 20:
    print("You're a teenager!")
elif age < 65:
    print("You're an adult!")
else:
    print("You're an elder!")
print("\n")
