#!/usr/bin/python
'''alien colours#2 (if-else chain)'''
alien_colour = 'green'
print("alien colour is green")
if alien_colour == 'green':
    print("You just earned 5 points!")
else:
    print("You just earned 10 points!")
print("\n")
alien_colour = 'yellow'
print("alien colour is yellow")
if alien_colour == 'green':
    print("You just earned 5 points!")
else:
    print("You just earned 10 points!")
